  double ecMax;
  double ecMin;
  double humiditymax;
  double humiditymin;
  double luxmax;
  double luxmin;
  double pHmax;
  double pHmin;
  double tempmax;
  double tempmin;

  PlantData(
      {required this.ecMax,
      required this.ecMin,
      required this.humiditymax,
      required this.humiditymin,
      required this.luxmax,
      required this.luxmin,
      required this.pHmax,
      required this.pHmin,
      required this.tempmax,
      required this.tempmin});
}
